<h1>
    Code of Conduct
</h1>

<p>
The Multiple Planet Systems Conference is committed to providing a safe, professional, and inclusive environment for all participants. Diversity and mutual respect are valued, and all attendees, speakers, volunteers, and organisers are expected to uphold these principles. Harassment, discrimination, or any form of exclusionary behaviour will not be tolerated. This Code of Conduct is aligned with the standards of similar scientific meetings.
</p>

<h2>
    Guidelines
</h2>

<ol>
  <li>
    Behave professionally and respectfully at all times. Harassment, sexist, racist, or otherwise exclusionary comments or jokes are not appropriate. Harassment includes verbal or physical abuse, inappropriate physical contact, sexual attention or innuendo, deliberate intimidation, stalking, recording or photography without consent, or offensive comments related to age, gender, sexual orientation, disability, physical appearance, race, nationality, or religion.
  </li>
  <li>
    All communication should be appropriate for a professional audience, including people with diverse backgrounds. Sexual or sexist language and imagery are not appropriate.
  </li>
  <li>
    Be respectful and do not insult or belittle other attendees, facilitators, or organisers. Critique ideas, not people.
  </li>
  <li>
    If you witness bullying, harassment, or aggression, you are encouraged to support the affected person and suggest reporting the behaviour. The choice to report lies with the affected person.
  </li>
  <li>
    If you share photos from talks or social events, please ensure you have permission from the people in the photos. Participants may share general meeting content on social media unless the speaker specifically requests otherwise.
  </li>
</ol>

<h2>
    Reporting
</h2>

<p>
If you experience or observe behaviour that violates this Code of Conduct, you may report it confidentially to designated contact persons on the Local Organising Committee. Reports will be handled with discretion, and retaliation against anyone reporting a violation will not be tolerated. Participants asked to stop inappropriate behaviour are expected to comply immediately.
</p>

<p>
For confidential reporting, please contact:
<strong>codeofconduct@exo-mps.eu</strong>
</p>

<h2>
    Consequences
</h2>

<p>
Participants who violate this Code of Conduct may be subject to appropriate action by the organisers, including warnings, removal from specific activities, or exclusion from the conference. Compliance with this Code of Conduct helps ensure a welcoming and respectful experience for all. 
</p>

