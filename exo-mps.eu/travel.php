<h1>
The city of Sofia
</h1>
<p>
Sofia is Bulgaria's capital and largest city, nestled at the foot of Vitosha mountain. The city combines rich Roman, Byzantine, and Ottoman heritage with a vibrant cultural life.
It is worthwhile to reserve some time for sightseeing. The Alexander Nevsky Cathedral, National Palace of Culture, Vitosha Boulevard, and the nearby Boyana Church are highly recommended.
The city of Sofia has its own web site with touristic and other information: <a href="https://www.visitsofia.bg/en/" target="_blank">visitsofia.bg</a>.
</p>

<h1>
    Entry Requirements &amp; Travel Documents
</h1>

<h3>European Union &amp; Schengen Area</h3>
<p>
Bulgaria has been a full member of the <a href="https://european-union.europa.eu/index_en" target="_blank">European Union (EU)</a> since 1 January 2007.
Since 1 January 2025, Bulgaria is a full member of the <a href="https://home-affairs.ec.europa.eu/policies/schengen-borders-and-visa/schengen-area_en" target="_blank">Schengen Area</a> (with no internal border checks).
</p>

<h3>Euro</h3>
<p>
Bulgaria adopted the Euro as its official currency on 1 January 2026, following approval by the European Commission and the European Central Bank in 2025 (having already met all convergence criteria).
</p>

<h3>Visa Information</h3>
<p>
For travellers from third countries, Schengen visas issued by Bulgaria after 31 March 2024 allow travel throughout the entire Schengen Area (up to 90 days within any 180-day period).
</p>
