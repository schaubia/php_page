<h1>
    About the meeting
</h1>

<p>
The conference aims to bring together experts and students with an interest in the 
dynamics of multiple-planet systems and planets in binary stars. 
The discussions will focus on both theoretical and observational aspects of these complex 
architectures, covering topics such as:
</p>
<ul>
    <li>Detection and characterization of multiple-planet systems</li>
    <li>Resonant and near-resonant configurations</li>
    <li>Secular dynamics and chaos</li>
    <li>Transit timing variations (TTVs)</li>
    <li>Three-dimensional architectures and Lidovâ€“Kozai effects</li>
    <li>Formation and stability of S-type and P-type planets in binaries</li>
    <li>Numerical tools for fitting exoplanet data, dynamical analysis, and long-term stability</li>        
    <li>Post-main-sequence evolution of multiple planet systems</li>
    and more!
</ul>
<p>
Our goal is to foster collaboration and exchange of ideas on the detection, 
origins, evolution, and dynamical stability of complex planetary systems. 
</p>
<p>
The meeting is limited to a maximum of 120 participants.
<p>

The conference will be held from 31 August to 4 September 2026 at 'The Egg' in the Rectorate building of <a href="https://www.uni-sofia.bg/index.php/eng" style="color:b" target="_blank"> Sofia University "St. Kliment Ohridski"</a>, Sofia, Bulgaria.
</p>
