<h1>
    About the meeting
</h1>

<p>
The conference aims to bring together experts and students working in the 
field of extrasolar planets and planetary dynamics. We hope to discuss variety of 
 dynamical problems such as:
</p>
<ul>
    <li>Detection, characterization and stability of multiple planet systems
    <li>Resonant and near-resonant pairs and chains
    <li>Secular dynamics
    <li>Chaos
    <li>TTV systems
    <li>Three-dimensional structure and Lidov-Kozai mechanisms
    <li>Formation and stability of S- and P- type planets in binaries   
    <li>Post-MS evolution of multiple planet systems
    <li>Solar system dynamics, etc.      
</ul>
<p>
The possible venue will be at the <a href="http://www.mpia.de/en" style="color:b" target="_blank"> American University in Bulgaria</a> (AUBG), Blagoevgrad, Bulgaria.
</p>
