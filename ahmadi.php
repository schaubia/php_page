<p>
Many questions remain unanswered in the field of high-mass star formation. Among them are those pertaining to the fragmentation properties of high-mass gas clumps and the disk-like structures that form within them. To investigate such topics, we have undertaken a NOEMA large program (CORE, PI: Henrik Beuther) for 18 high-mass star forming clumps in the northern sky, resolving structures at mm wavelength on scales of ~600AU (~0.3‚Äù). Our observations are complemented with data from the 30m telescope in order to recover missing flux and probe large scale structures. In this talk, I will give an overview of the setup of the CORE project and its current status, along with preliminary results of a case study for W3(H2O). With a broad range of observed molecules, we are able to disentangle different disk/core contributions and see two fragments within W3(H2O). A stability analysis of the larger rotating structure around this clump furthermore proves it to be Toomre-unstable in parts, explaining the fragmentation we observe.	
