<h2>About the format</h2>

<p>
The workshop format is designed to encourage interactions and collaborations,
with a significant fraction of the time dedicated to <i>unconference</i> sessions, which are dynamic
participant-driven sessions covering discussions, informal presentations,
collaborative work sessions, and tutorials (for instance on how to use
specific visualization tools), and the remainder of the time dedicated to
formal talks as well as plenary discussion sessions. Unconference sessions are
proposed for and voted on by participants at the meeting itself, ensuring a
dynamic program.
</p>


