<h3>
By airplane<br />
</h3>

<br />
The closest airport is Frankfurt International Airport (FRA). 
From there, you can take the train to Heidelberg. 
Note that there are two train stations at the airport: 
one for regional trains and one for long-distance trains (the latter is called Fernbahnhof). 
From the regional train station you can take the train to Frankfurt am Main Hauptbahnhof 
(the main station of the city of Frankfurt itself), and from there you can take a 
direct train to Heidelberg. Or you take the ICE train from Frankfurt Flughafen 
Fernbahnhof to Mannheim (30 minutes), and then from there take the regional train to Heidelberg (15 minutes).
<br />
You can also reserve a shuttle bus from TLS. This will be more convenient, as 
it will bring you straight to your hotel.<br />
