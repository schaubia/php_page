<h1>
The city of Heidelberg
</h1>
<p>
The conference will take place in the historic city of Heidelberg, Germany. 
Heidelberg is an university town, with an old town squeezed in a V-type vally of the Neckar 
river between forested hills, and a newer part of town sprawling into the plains of the Rhine valley. 
It is worthwhile to reserve some time for sightseeing. The castle, the old bridge, the old town and (on the other side of the Neckar river) 
the "philosophers path" (Philosophenweg) are highly recommended.
The city of Heidelberg has its own web site with touristic (and other) information: <a href="http://www.heidelberg.de" target="_blank">www.heidelberg.de</a>.
</p>

