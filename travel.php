<h3>
Private transportation for sightseeing<br />
</h3>
<br />
If you want to do sightseeing (highly recommended), then you can make use of various ways of 
transportation. Heidelberg is a small town, so everything can be done on foot. 
But you can also use the buses or trams. Tickets can be bought at the ticket machines at most stops.
 There is also a fernicular from the old town to the castle and further up to the top of the Königstuhl hill.<br />
<br />
Buses and trams (Strassenbahn or Stadtbahn) are the main form of transportation in Heidelberg, 
with the center hub at Bismarktplatz in the Altstadt. Individual tickets are available, 
but there are many special passes that make it much more affordable and efficient to get around. 
Individual tickets can be purchased at ticket machines or with the driver, and passes can be
 purchased at the ticket counters. Maps of each bus and tram route can be found at every stop.<br />
<br />
The size of Heidelberg is relatively small, making it is easy to go wherever you want in a short period of time. Using a bicycle is a great way to do this, though bicycle traffic in Heidelberg can be a bit chaotic. Many streets or walkways have bike lanes, but some do not and can be rather narrow, especially in the old town.
<br />
There are several places where renting a bike is possible. Some hotels also offer bike rentals to their guests.<br />
